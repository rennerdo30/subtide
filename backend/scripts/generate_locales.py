def main():
    print(f"Loading English messages from {LOCALES_DIR}/en/messages.json")
    en_messages = load_english_messages()
    
    for locale in ALL_LOCALES:
        if locale == 'en': continue 
        
        locale_dir = os.path.join(LOCALES_DIR, locale)
        os.makedirs(locale_dir, exist_ok=True)
        
        file_path = os.path.join(locale_dir, 'messages.json')
        existing_messages = {}
        
        if os.path.exists(file_path):
            try:
                with open(file_path, 'r', encoding='utf-8') as f:
                    existing_messages = json.load(f)
            except:
                print(f"Warning: Could not read existing {locale} messages")

        new_messages = existing_messages.copy()
        
        # Determine fallback translation dictionary
        # 1. Exact match (e.g. zh_CN)
        # 2. Base match (e.g. zh)
        translation_dict = None
        if locale in TRANSLATIONS:
            translation_dict = TRANSLATIONS[locale]
        else:
            base_lang = locale.split('_')[0]
            if base_lang in TRANSLATIONS:
                translation_dict = TRANSLATIONS[base_lang]

        for key, value in en_messages.items():
            # Check if we have a translation in our dictionary
            tr_val = None
            if translation_dict and key in translation_dict:
                tr_val = translation_dict[key]
            
            if tr_val:
                # Use our explicit translation (overwriting if necessary)
                new_messages[key] = {"message": tr_val}
            elif key not in new_messages:
                # Key missing and no translation: Fallback to English
                new_messages[key] = value

        # Write back
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(new_messages, f, ensure_ascii=False, indent=4)
            
        print(f"Updated {locale}")

if __name__ == "__main__":
    main()
